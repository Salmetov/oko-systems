def detect_ui_lang(params: dict | None = None, accept_language: str = '') -> str:
    params = params or {}
    requested = str((params.get('lang') or [''])[0]).strip().lower()
    if requested in SUPPORTED_UI_LANGS:
        return requested
    header = str(accept_language or '').lower()
    for raw_part in header.split(','):
        lang_tag = raw_part.split(';', 1)[0].strip().replace('_', '-')
        primary = lang_tag.split('-', 1)[0]
        if primary in SUPPORTED_UI_LANGS:
            return primary
    return 'ru'


def t(lang: str, key: str, **kwargs) -> str:
    lang_code = lang if lang in SUPPORTED_UI_LANGS else 'ru'
    template = UI_TEXT.get(lang_code, {}).get(key) or UI_TEXT['ru'].get(key) or key
    if kwargs:
        try:
            return template.format(**kwargs)
        except Exception:
            return template
    return template


def translate_block_name(name: str, lang: str) -> str:
    raw = str(name or '').strip()
    short_ru = BLOCK_SHORT_NAMES_RU.get(raw, raw)
    if lang == 'kk':
        return BLOCK_TRANSLATIONS_KK.get(short_ru, BLOCK_TRANSLATIONS_KK.get(raw, short_ru))
    return short_ru


def translate_module_name(name: str, lang: str) -> str:
    raw = normalize_module_title(str(name or '').strip())
    short_ru = MODULE_SHORT_NAMES_RU.get(raw, raw)
    if lang == 'kk':
        return MODULE_TRANSLATIONS_KK.get(short_ru, MODULE_TRANSLATIONS_KK.get(raw, short_ru))
    return short_ru
